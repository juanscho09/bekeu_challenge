     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $response = [
            'error' => false,
            'mensaje' => ""
        ];

        try{
            $file = public_path('files/states.csv');
            Excel::import(new StateImport, $file);
            $response['mensaje'] = "Importación correcta.";
            // $response['mensaje'] = public_path();
        } catch(\Exception $ex){
            $response = [
                'error' => true,
                'mensaje' => "El archivo ya fue cargado o hubo un error en la importación."
            ];
        }

        return response()->json($response);
    }

